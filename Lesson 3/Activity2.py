Food = "Pizza"
Slices = 12
Weight = 3.14
Pepperoni = True

print("The food is:", Food)
print("The data type of food:", type(Food))

print("The amount of slices is:", Slices)
print("The data type of Slices:", type(Slices))

print("The weight is:", Weight)
print("The data type of Weight:", type(Weight))

print("Is it pepperoni pizza?:", Pepperoni)
print("The data type of Pepperoni:", type(Pepperoni))

print("Typecasting")
Food = str(Food)
Slices = str(Slices)
Weight = str(Weight)
Pepperoni = str(Pepperoni)

print('Data type of Food:', type(Food))
print('Data type of Slices:', type(Slices))
print('Data type of Weight:', type(Weight))
print('Data type of Pepperoni:', type(Pepperoni))